# maximum and minimum number in a list

marks = [54, 67, 89, 23, 90, 45]

print("List:", marks)
print("Maximum =", max(marks))  # max() gives highest value in the list
print("Minimum =", min(marks))  # min() gives lowest value

# Output:
# List: [54, 67, 89, 23, 90, 45]
# Maximum = 90
# Minimum = 23