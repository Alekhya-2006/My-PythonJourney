# Access list values using indexing

fruits = ["apple", "banana", "mango", "grapes"]

print("First element:", fruits[0])
print("Last element:", fruits[-1]) # negarive indexing
print("Second element:", fruits[1])
print("Third element:", fruits[2])

# Output:
# First element: apple
# Last element: grapes
# Second element: banana
# Third element: mango